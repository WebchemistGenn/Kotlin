package com.example.kotlin

/**
 * 2. 변수
 * val (value) - 변경불가
 * var (variable) - 변경가능
 */

fun main() {
    val number01: Int = 10
    var number02: Int = 6
    val number03 = 100 // 변수 선언을 하지 않아도 자동으로 인식합니다.




    val firstName = "Yoon"
    val lastName = "WonYoul"

    val age = 30

    println("My name is $firstName $lastName")
    println("${age}세 입니다.")
}